/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // immediately invoke the function `fn` with the provided arguments `args`.
    fn(...args);
    
    // set up an interval timer that repeatedly calls `fn` with `args` every `t` milliseconds.
    const timer = setInterval(() => {
        fn(...args);
    }, t);
    
    // function that clears the interval timer, stopping further executions of `fn`.
    const cancel = () => clearInterval(timer);
    
    return cancel;
};

